print(77.2)
print(8 + 5.5)
print(8 - 5)
print(8 / 5)
# 整數除法 //
print(8 // 5)

# 取餘數 %   (8 / 5 = 1...3)
print(8 % 5)

# 數字轉字串函式 str(數字)
print('我要印數字' + str(8))

# 取絕對值函式 abs(數字)
print(abs(-33))

# 取次方函式 pow(底數,次方數)
print(pow(2,4))

# 最大值函式 max(數字,數字,...,數字)
# 最小值函式 min(數字,數字,...,數字)
print(max(1,2,3,44,555,1000))
print(min(1,22,33,5000)) 

# 四捨五入函式 round(數字)
print(round(1.4))
print(round(1.5))